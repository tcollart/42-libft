#include "libft.h"

int					ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*ptr1;
	unsigned char	*ptr2;

	ptr1 = (unsigned char *)s1;
	ptr2 = (unsigned char *)s2;
	if (n == 0)
		return (0);
	if (*ptr1 != *ptr2)
		return (*ptr1 - *ptr2);
	return (ft_memcmp((void *)(s1 + 1), (void *)(s2 + 1), (n - 1)));
}
